void print_binary_value_image(uint8_t* data, int width, int height) {
    printf("Binary value image:\n");
    for (int i=0; i<height; i++) {
        for (int j=0; j<width; j++) {
            printf("%s", data[i*width+j] ? "#" : ".");
        }
        printf("\n");
    }
    printf("\n");
}

